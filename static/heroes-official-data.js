(function mergeOfficialHeroesData() {
    function cleanName(value) {
        return String(value || '').replace(/<[^>]*>/g, ' ').replace(/\s+/g, ' ').trim().toUpperCase();
    }

    var legacyByName = {};
    (window.heroesData || []).forEach(function(hero) {
        legacyByName[cleanName(hero.nickname)] = hero;
    });

    window.heroesData = officialHeroesData.map(function(hero) {
        var legacy = legacyByName[cleanName(hero.nickname)] || {};
        return Object.assign({}, legacy, hero, {
            name: legacy.name || hero.name,
            intro: legacy.intro || hero.intro,
            story: legacy.story || hero.story,
            careerIconName: hero.careerIconName.trim(),
            roleTags: hero.roleTags && hero.roleTags.length ? hero.roleTags : [hero.careerIconName.trim()]
        });
    }).sort(function(a, b) {
        return cleanName(a.nickname).localeCompare(cleanName(b.nickname));
    });
})();
